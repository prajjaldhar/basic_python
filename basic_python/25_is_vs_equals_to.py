#is vs ==
print(1 == "1")#type are different
print([] == [])#same values ie. empty
print([1,2,32]==[1,2,32])#same values 
print(10.00==10.00)
print(True==1)
print(()==())#same values ie. empty
print({}=={})#same values ie. empty
print("\n")
#when we used is
print("\n")
print(1 is "1")#type are different
print([]is [])#different data structure
print([1,2,32] is [1,2,32])#same values 
print(10.00 is 10.00)
print(True is 1)
print(()is())#different data structure
print({} is {})#different data structure
